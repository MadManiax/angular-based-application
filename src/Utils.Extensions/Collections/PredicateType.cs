
namespace Utils.Extensions.Collections
{
    /// <summary>
    /// Operatori di confronto
    /// </summary>
    public enum PredicateType
    {
        /// <summary>
        /// Uguale
        /// </summary>
        Equal,
        /// <summary>
        /// Strettamente maggiore
        /// </summary>
        GreaterThan,
        /// <summary>
        /// Maggiore uguale
        /// </summary>
        GreaterThanOrEqual,
        /// <summary>
        /// Strettamente minore
        /// </summary>
        LessThan,
        /// <summary>
        /// Minore o uguale
        /// </summary>
        LessThanOrEqual,
        /// <summary>
        /// Diverso da
        /// </summary>
        NotEqual
    }
}
